export interface ShapeConfig {

  /* X position of the shape */
  x: number;

  /* Y position of the shape */
  y: number;

  /* Size (thickness) of the shape */
  size: number;

  /* Radius of the shape */
  radius: number;

  /* Total shape gutters */
  gutters: number;

  /* Color of the shape's fill path */
  fillColor: string;

  /* Color of the shape's track path */
  trackColor: string;

  /* Color of the shape's follow path */
  followColor: string;

}
