export interface ShapeConfig {

  /* X position of the shape */
  x: number;

  /* Y position of the shape */
  y: number;

  /* Size of the shape */
  size: number;

  /* Radius of the shape */
  radius: number;

  /* Gutter of the shape */
  gutter: number;

  /* Color of the shape's coverage */
  fillColor: string;

  /* Color of the shape's track */
  trackColor: string;

}
